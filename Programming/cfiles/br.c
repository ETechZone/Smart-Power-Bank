//
//*********************************************************************************************
//Function Name    : Uint8* BR_screenmap()
//No.Of Parameters : 0
//Parameters Type  : void
//Return Type      : Uint8*
//Description      : To get bitmap of the BR screen
//**********************************************************************************************
//
Uint8* BR_screenmap(){
    Current_Screen_ptr=&Current_Screen[0];
    //br_main(0);
	switch(Get_BR_Screen_val()){
		case 0x01:{
		br_2k(256);
       	br_3k(512);
		br_back(768);	
		break;	
		}
		case 0x02:{
		br_3k(256);
		br_back(512);	
		br_blank(768);
		break;		
		}
		case 0x03:{
		br_back(256);
        br_blank(512);
		br_blank(768);
		break;		
		}
		default:{
	    br_1k(256);
		br_2k(512);
       	br_3k(768);		
		break;	
		}
	}
	return Current_Screen_ptr;
}



