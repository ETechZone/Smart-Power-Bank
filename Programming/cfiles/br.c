//Parameters Type  : Uint8
//Return Type      : void
//Description      : To Update BR status of screen
//**********************************************************************************************
//
void BR_UP_update(){
    Uint8 i;
    i = Get_BR_Screen_val();
    i--;
    if(Get_BR_Screen_val()!=0x00){
        br_src.status=i;
    }
}
//
//*********************************************************************************************
//Function Name    : BR_DOWN_update()
//No.Of Parameters : 1
//Parameters Type  : Uint8
//Return Type      : void
//Description      : To Update BR status of screen
//**********************************************************************************************
//
void BR_DOWN_update(){
    Uint8 i;
    i=Get_BR_Screen_val();
    i++;
    if(Get_BR_Screen_val()!=0x03)
    br_src.status=i;
}

